#for addition:
def calculate_addition(a,b):
    return a+b

#for substraction: 
def calculate_substraction(a,b):
    return a-b
#for multiplication :
def calculate_multiplication(a,b):
    return a*b

#for division :
def calculate_division(a,b):  
    return a/b

# for exponent

def calculate_exponent(a,b):
    return a**b